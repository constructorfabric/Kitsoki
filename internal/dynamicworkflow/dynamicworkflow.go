func mustHashFile(path string) string {
	b, err := os.ReadFile(path)
	if err != nil {
		return ""
	}
	sum := sha256.Sum256(b)
	return "sha256:" + hex.EncodeToString(sum[:])
}

// HashFile returns a stable sha256 digest of path's bytes, or "" when the file
// cannot be read.
func HashFile(path string) string { return mustHashFile(path) }

