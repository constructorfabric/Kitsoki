func pathExists(root, rel string) bool {
	if rel == "" {
		return false
	}
	if filepath.IsAbs(rel) {
		_, err := os.Stat(rel)
		return err == nil
	}
	_, err := os.Stat(filepath.Join(root, filepath.FromSlash(rel)))
	return err == nil
}

func copyDir(src, dst string) error {
	return filepath.WalkDir(src, func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}
		rel, relErr := filepath.Rel(src, path)
		if relErr != nil {
			return relErr
		}
		target := filepath.Join(dst, rel)
		if d.IsDir() {
			return os.MkdirAll(target, 0o755)
		}
		return copyFile(path, target)
	})
}

func copyFile(src, dst string) error {
	b, err := os.ReadFile(src)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(dst), 0o755); err != nil {
		return err
	}
	return os.WriteFile(dst, b, 0o644)
}

func workflowSlug(slug, goal string) string {
	if s := strings.TrimSpace(slug); s != "" {
		return sanitizeSlug(s)
	}
	return sanitizeSlug(goal)
}

func sanitizeSlug(s string) string {
	s = strings.ToLower(strings.TrimSpace(s))
	s = strings.ReplaceAll(s, " ", "-")
	s = unsafeSlug.ReplaceAllString(s, "-")
	s = strings.Trim(s, "-_.")
	if s == "" {
		return "workflow"
	}
	return s
}

func truncateWords(s string, max int) string {
	if max <= 0 {
		return strings.TrimSpace(s)
	}
	parts := strings.Fields(strings.TrimSpace(s))
	if len(parts) <= max {
		return strings.Join(parts, " ")
	}
	return strings.Join(parts[:max], " ") + " ..."
}

func quoteArg(s string) string {
	if s == "" {
		return "''"
	}
	if strings.ContainsAny(s, " \t\n'\"") {
		return "'" + strings.ReplaceAll(s, "'", "'\\''") + "'"
	}
	return s
}

func isLLMSpendingCommand(cmd string) bool {
	lowered := strings.ToLower(strings.TrimSpace(cmd))
	if lowered == "" {
		return false
	}
	patterns := []string{"claude", "codex", "openai", "anthropic", "kitsoki tour", "harness: live", "--harness live"}
	for _, p := range patterns {
		if strings.Contains(lowered, p) {
			return true
		}
	}
	return false
}

func isBaseStoryPath(target string) bool {
	if target == "" {
		return false
	}
	clean := filepath.ToSlash(filepath.Clean(target))
	return strings.Contains(clean, "/internal/basestories/stories/") || strings.HasPrefix(clean, "internal/basestories/stories/")
}

